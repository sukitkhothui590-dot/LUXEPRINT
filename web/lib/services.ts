export function getServiceBySlug(slug: string): Service | undefined {
  return services.find((s) => s.id === slug);
}

export function getPricesForService(service: Service): PriceRow[] {
  return service.prices ?? defaultPrices;
}

export function getPrepForService(service: Service): PrepItem[] {
  return service.prepItems ?? defaultPrepItems;
}
